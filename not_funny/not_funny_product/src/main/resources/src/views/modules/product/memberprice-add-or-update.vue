<template>
  <el-dialog
    :title="!dataForm.id ? '新增' : '修改'"
    :close-on-click-modal="false"
    :visible.sync="visible">
    <el-form :model="dataForm" :rules="dataRule" ref="dataForm" @keyup.enter.native="dataFormSubmit()" label-width="80px">
    <el-form-item label="" prop="productId">
      <el-input v-model="dataForm.productId" placeholder=""></el-input>
    </el-form-item>
    <el-form-item label="" prop="memberLevelId">
      <el-input v-model="dataForm.memberLevelId" placeholder=""></el-input>
    </el-form-item>
    <el-form-item label="会员价格" prop="memberPrice">
      <el-input v-model="dataForm.memberPrice" placeholder="会员价格"></el-input>
    </el-form-item>
    <el-form-item label="" prop="memberLevelName">
      <el-input v-model="dataForm.memberLevelName" placeholder=""></el-input>
    </el-form-item>
    </el-form>
    <span slot="footer" class="dialog-footer">
      <el-button @click="visible = false">取消</el-button>
      <el-button type="primary" @click="dataFormSubmit()">确定</el-button>
    </span>
  </el-dialog>
</template>

<script>
  export default {
    data () {
      return {
        visible: false,
        dataForm: {
          id: 0,
          productId: '',
          memberLevelId: '',
          memberPrice: '',
          memberLevelName: ''
        },
        dataRule: {
          productId: [
            { required: true, message: '不能为空', trigger: 'blur' }
          ],
          memberLevelId: [
            { required: true, message: '不能为空', trigger: 'blur' }
          ],
          memberPrice: [
            { required: true, message: '会员价格不能为空', trigger: 'blur' }
          ],
          memberLevelName: [
            { required: true, message: '不能为空', trigger: 'blur' }
          ]
        }
      }
    },
    methods: {
      init (id) {
        this.dataForm.id = id || 0
        this.visible = true
        this.$nextTick(() => {
          this.$refs['dataForm'].resetFields()
          if (this.dataForm.id) {
            this.$http({
              url: this.$http.adornUrl(`/product/memberprice/info/${this.dataForm.id}`),
              method: 'get',
              params: this.$http.adornParams()
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.dataForm.productId = data.memberPrice.productId
                this.dataForm.memberLevelId = data.memberPrice.memberLevelId
                this.dataForm.memberPrice = data.memberPrice.memberPrice
                this.dataForm.memberLevelName = data.memberPrice.memberLevelName
              }
            })
          }
        })
      },
      // 表单提交
      dataFormSubmit () {
        this.$refs['dataForm'].validate((valid) => {
          if (valid) {
            this.$http({
              url: this.$http.adornUrl(`/product/memberprice/${!this.dataForm.id ? 'save' : 'update'}`),
              method: 'post',
              data: this.$http.adornData({
                'id': this.dataForm.id || undefined,
                'productId': this.dataForm.productId,
                'memberLevelId': this.dataForm.memberLevelId,
                'memberPrice': this.dataForm.memberPrice,
                'memberLevelName': this.dataForm.memberLevelName
              })
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.$message({
                  message: '操作成功',
                  type: 'success',
                  duration: 1500,
                  onClose: () => {
                    this.visible = false
                    this.$emit('refreshDataList')
                  }
                })
              } else {
                this.$message.error(data.msg)
              }
            })
          }
        })
      }
    }
  }
</script>
